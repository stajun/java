package day2;

public class exam04 {

	public static void main(String[] args) {
		/*
		 * num1라는 정수형 변수를 선언하고,  num1에 1을 저장
		 * num2라는 정수형 변수를 선언하고, num2에 2를 저장
		 * ch 라는 문자형 변수를 선언하고 ch에 +를 저장 한 후
		 * 콘솔에 1 + 2가 출력되도록 코드를 작성하세요.
		 */
		int num1 = 1, num2 = 2;
		char ch = '+';
		System.out.println(""+num1 + ch + num2);
	}

}
